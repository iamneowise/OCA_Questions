
public class SwitchGrade {

	public static void main(String[] args) {
		String Grade="B";
		switch(Grade) {
		case "A":case "1":
			System.out.println("First Grade");
			break;
		case "B":
			System.out.println("Second Grade");
			break;
			default:
				System.out.println("Third Grade");
		}

	}

}
